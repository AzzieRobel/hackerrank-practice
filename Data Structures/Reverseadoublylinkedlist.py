def reverse(llist):
    """
    Reverse a doubly linked list in place and return the new head.
    
    Args:
        llist: head node of the doubly linked list
        
    Returns:
        DoublyLinkedListNode: new head of the reversed list
    """
    # Handle empty list
    if not llist:
        return None
    
    current = llist
    new_head = None
    
    # Traverse the list and swap next/prev pointers for each node
    while current:
        # Swap next and prev pointers
        current.next, current.prev = current.prev, current.next
        
        # Move to the next node (which is now prev due to swap)
        new_head = current  # Track the last processed node as new head
        current = current.prev  # Move to what was originally the next node
    
    return new_head

